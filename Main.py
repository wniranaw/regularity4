def setdot(*args):
    args = list(args)
    for i in range(len(args)):
        if type(args[i]) != set:
            args[i] = {args[i]}
    result = args[0]
    for i in range(1, len(args)):
        temp = []
        for x in result:
            for y in args[i]:
                temp.append(dot(x, y))
        result = set(temp)
    return result


